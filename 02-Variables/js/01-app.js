// inicializar una variable e imprimirla en la consola
var producto ="Teclado";
console.log(producto);

// las variables se pueden reasignar
producto = "TecladoGamer";
console.log(producto);
producto = "TecladoGamerPRO";
console.log(producto); 

// Java Script es un lenguaje de tipo dinamico, no se especifica el tipo de dato que se va a almacenar en la variable
producto = 30;
console.log(producto);

// Se puede inicializar una variable sin valor
var disponible;
disponible=true;
console.log(disponible);
disponible=false;
console.log(disponible);
